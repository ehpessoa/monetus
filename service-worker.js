
const CACHE_NAME = 'monetus-v1';
const URLS_TO_CACHE = [
  '/',
  '/index.html',
  '/manifest.json'
];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then((cache) => {
        // Tenta fazer cache dos recursos essenciais
        return cache.addAll(URLS_TO_CACHE).catch(err => {
            console.warn('Falha ao fazer cache de alguns recursos na instalação:', err);
        });
      })
  );
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames.map((cacheName) => {
          if (cacheName !== CACHE_NAME) {
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
  self.clients.claim();
});

self.addEventListener('fetch', (event) => {
  // Estratégia simples: Cache falling back to network
  // Para uma aplicação de finanças, talvez queiramos network-first para dados,
  // mas como os dados estão no localStorage, o cache-first para os assets estáticos é seguro.
  event.respondWith(
    caches.match(event.request)
      .then((response) => {
        if (response) {
          return response;
        }
        return fetch(event.request);
      })
  );
});
